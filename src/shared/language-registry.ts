const LANGUAGE_PATTERNS: Array<[RegExp, LanguageId]> = [
  [/^(dockerfile|containerfile)\..+$/, 'dockerfile'],
  [/^jenkinsfile.*$/, 'groovy'],
  [/^compose(?:\..+)?\.ya?ml$/, 'dockercompose'],
  [/^.*docker.*compose.*\.ya?ml$/, 'dockercompose'],
  [/^tsconfig[.-].*\.json$/, 'jsonc'],
  [/^jsconfig[.-].*\.json$/, 'jsonc'],
  [/^snippets.*\.json$/, 'snippets'],
  [/^.*\.log\..$/, 'log'],
  [/\/(?:\.config\/git|\.git)\/config$/, 'properties'],
  [/\/rebase-merge\/done$/, 'git-rebase'],
];

const SORTED_EXTENSIONS = Object.keys(EXT_TO_LANGUAGE).sort((a, b) => b.length - a.length);

export function languageIdFromPath(filePath: string): LanguageId {
  const normalizedPath = filePath.replace(/\\/g, '/').toLowerCase();
  const slash = normalizedPath.lastIndexOf('/');
  const filename = slash >= 0 ? normalizedPath.slice(slash + 1) : normalizedPath;

  const filenameLanguage = FILENAME_TO_LANGUAGE[filename as keyof typeof FILENAME_TO_LANGUAGE];
  if (filenameLanguage) return filenameLanguage;

  for (const [pattern, languageId] of LANGUAGE_PATTERNS) {
    if (pattern.test(filename) || pattern.test(normalizedPath)) return languageId;
  }

  const extension = SORTED_EXTENSIONS.find((candidate) => normalizedPath.endsWith(candidate));
  if (extension) return EXT_TO_LANGUAGE[extension as keyof typeof EXT_TO_LANGUAGE];

  return 'plaintext';
}

export function hasLanguageServer(languageId: LanguageId): languageId is ServerLanguageId {
  return LANGUAGE_SERVERS[languageId] !== null;
}
